# 非线性变换τ
def tau(a):
    a = a & 0xFFFFFFFF  # 截断输入到32位范围
    x0 = (a >> 24) & 0xFF
    x1 = (a >> 16) & 0xFF
    x2 = (a >> 8) & 0xFF
    x3 = a & 0xFF
    return (SM4_SBOX[x0] << 24) | (SM4_SBOX[x1] << 16) | (SM4_SBOX[x2] << 8) | SM4_SBOX[x3]


# 合成变换T
def T(a):
    return L(tau(a))


# 轮函数F
def F(X0, X1, X2, X3, rk):
    return (X0 ^ T(X1 ^ X2 ^ X3 ^ rk)) & 0xFFFFFFFF  # 截断结果


# 密钥扩展函数
def sm4_key_expansion(key):
    if len(key) != 16:
        raise ValueError("SM4密钥必须为16字节")
    # 将密钥转换为4个32位字
    K = [int.from_bytes(key[i * 4:(i + 1) * 4], byteorder='big') & 0xFFFFFFFF for i in range(4)]
    # 与FK异或
    K = [K[i] ^ FK[i] for i in range(4)]
    rk = [0] * 32
    for i in range(32):
        temp = K[1] ^ K[2] ^ K[3] ^ CK[i]
        temp = tau(temp)
        temp = L(temp)
        rk[i] = (K[0] ^ temp) & 0xFFFFFFFF  # 截断轮密钥
        # 更新K，同样截断
        K = [K[1] & 0xFFFFFFFF, K[2] & 0xFFFFFFFF, K[3] & 0xFFFFFFFF, rk[i]]
    return rk


# SM4加密函数
def sm4_encrypt(plaintext, key, rk=None):
    if len(plaintext) != 16:
        raise ValueError("SM4明文块必须为16字节")
    # 生成轮密钥
    if rk is None:
        rk = sm4_key_expansion(key)
    # 将明文转换为4个32位字，并截断
    X = [int.from_bytes(plaintext[i * 4:(i + 1) * 4], byteorder='big') & 0xFFFFFFFF for i in range(4)]
    # 32轮迭代
    for i in range(32):
        new_X0 = X[1]
        new_X1 = X[2]
        new_X2 = X[3]
        new_X3 = F(X[0], X[1], X[2], X[3], rk[i])
        X = [new_X0, new_X1, new_X2, new_X3]
    # 最终交换
    X = [X[3], X[2], X[1], X[0]]
    # 转换为密文，确保每个整数在32位范围
    return b''.join([x.to_bytes(4, byteorder='big') for x in X])


