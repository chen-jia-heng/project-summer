# 线性变换L
def L(t):
    t = t & 0xFFFFFFFF  # 截断到32位无符号整数范围
    res = t ^ (((t << 2) | (t >> 30)) & 0xFFFFFFFF)
    res ^= (((t << 10) | (t >> 22)) & 0xFFFFFFFF)
    res ^= (((t << 18) | (t >> 14)) & 0xFFFFFFFF)
    res ^= (((t << 24) | (t >> 8)) & 0xFFFFFFFF)
    return res & 0xFFFFFFFF  # 再次截断确保范围


